class AgentCodeGenerator:
    """
    Compiles a validated AgentDefinition into a production Python file.
    """

    def __init__(self) -> None:
        self._env = Environment(
            loader=BaseLoader(), trim_blocks=True, lstrip_blocks=True
        )
        self._template = self._env.from_string(AGENT_TEMPLATE)

    def generate(
        self,
        agent: AgentDefinition,
        definition_path: str = "agent.yaml",
        output_path: Optional[str | Path] = None,
    ) -> str:
        """Render the Jinja2 template and return the generated Python source."""

        # ── Derive trigger config ──
        triggers = agent.triggers.event_types or []
        trigger_list = repr(list(triggers))
        schedule_interval = (
            agent.triggers.schedule.interval if agent.triggers.schedule else None
        )

        # ── Derive data sources ──
        sources = agent.data_collection.sources or []
        sources_list = repr(list(sources))
        time_window = agent.data_collection.time_window

        # ── Derive reasoning ──
        ai_models_list = (
            repr(list(agent.reasoning.ai_models)) if agent.reasoning else repr([])
        )

        # ── FIX: Derive consensus method as plain string value ──
        # Previously the template used `{{ agent.consensus_validation.method }}`
        # which, with Pydantic v2's enum handling, could render as
        # "ConsensusMethod.WEIGHTED_VOTE" (the repr) instead of "weighted_vote"
        # (the value). This caused a ValueError in ConsensusValidator.__init__
        # when the generated agent was instantiated.
        if agent.consensus_validation:
            method_raw = agent.consensus_validation.method
            # Handle both enum objects and plain strings defensively
            consensus_method_value = (
                method_raw.value if hasattr(method_raw, "value") else str(method_raw)
            )
        else:
            consensus_method_value = "none"

        # ── Derive consensus weights ──
        if agent.consensus_validation and agent.consensus_validation.weights:
            consensus_weights = repr(dict(agent.consensus_validation.weights))
        elif agent.reasoning and agent.reasoning.ai_models:
            # Auto-assign equal weights
            n = len(agent.reasoning.ai_models)
            equal_w = round(1.0 / n, 4) if n else 0.0
            consensus_weights = repr({m: equal_w for m in agent.reasoning.ai_models})
        else:
            consensus_weights = repr({})

        # ── Derive actions ──
        actions_list = repr(list(agent.actions or []))

        # ── SEC FIX (CQH-SEC-001): sanitize / quote every free-text scalar ──
        # Free-text fields (reasoning.task, metadata.description, metadata.owner,
        # version, max_runtime, approval_required, time_window, schedule interval)
        # were previously interpolated RAW into Python string literals and
        # docstrings. A crafted value such as  x"; import os; os.system(...) #
        # broke out of the literal and executed on import of the generated
        # module. We now:
        #   * emit every Python-literal field via repr() (a complete, correctly
        #     escaped literal — no surrounding quotes in the template), and
        #   * strip docstring/comment terminators from any text placed inside a
        #     triple-quoted docstring so it cannot close the docstring early.
        def _doc_safe(value: Any) -> str:
            """Neutralise text destined for a triple-quoted docstring/comment."""
            text = "" if value is None else str(value)
            # Remove characters that could terminate a docstring or start code.
            text = text.replace('"""', '').replace("'''", "")
            text = text.replace("\\", "/").replace('"', "'")
            # Collapse newlines so a payload cannot span out of the docstring.
            text = " ".join(text.split())
            return text[:500]

        task_value = agent.reasoning.task if agent.reasoning else None
        approval_value = (
            agent.safety_controls.approval_required
            if agent.safety_controls.approval_required is not None
            else "none"
        )
        # Enums render via their value, not their repr.
        approval_value = getattr(approval_value, "value", approval_value)

        safe_ctx = {
            "safe_name": _doc_safe(agent.name),
            "safe_class_name": agent.name,  # regex-validated identifier upstream
            "safe_version": _doc_safe(agent.version),
            "safe_version_literal": repr(str(agent.version)),
            "safe_description": _doc_safe(agent.metadata.description),
            "safe_owner": _doc_safe(agent.metadata.owner),
            "safe_priority": _doc_safe(getattr(agent.metadata.priority, "value",
                                               agent.metadata.priority)),
            "safe_task_doc": _doc_safe(task_value) if task_value else "N/A",
            "safe_task_literal": repr(str(task_value)) if task_value else repr("none"),
            "safe_time_window": repr(str(time_window)),
            "safe_max_runtime": repr(str(agent.safety_controls.max_runtime)),
            "safe_approval_required": repr(str(approval_value)),
            "safe_schedule_interval": (
                repr(str(schedule_interval)) if schedule_interval else repr(None)
            ),
        }

        context = {
            "agent": agent,
            "definition_path": definition_path,
            "trigger_list": trigger_list,
            "schedule_interval": schedule_interval,
            "sources_list": sources_list,
            "time_window": time_window,
            "ai_models_list": ai_models_list,
            "consensus_method_value": consensus_method_value,
            "consensus_weights": consensus_weights,
            "actions_list": actions_list,
            **safe_ctx,
        }

        code = self._template.render(**context)

        if output_path:
            output_path = Path(output_path)
            output_path.parent.mkdir(parents=True, exist_ok=True)
            output_path.write_text(code, encoding="utf-8")

        return code

    def generate_from_file(
        self,
        definition_path: str | Path,
        output_dir: str | Path = "generated_agents",
    ) -> Path:
        """
        Convenience wrapper: validate YAML → generate Python → write to disk.
        Returns the path to the generated file.
        """
        import yaml
        from acda.compiler.schema_validator import AdfValidator
        from acda.models.schemas import AdfDocument

        definition_path = Path(definition_path)
        validator = AdfValidator()
        result, agent = validator.validate_file(definition_path)

        if not result.valid:
            result.print_report()
            raise ValueError(f"Validation failed for {definition_path}")

        assert agent is not None

        output_dir = Path(output_dir)
        output_file = output_dir / f"{agent.name.lower()}.py"

        self.generate(agent, str(definition_path), output_file)
        return output_file
